template<typename T>
T gcd(T a, T b) {
    while (b) {
        a %= b;
        swap(a, b);
    }
    return a;
}

template<typename T>
T lcm(T a, T b) {
    return a / gcd(a, b) * b;
}

class Solution {
public:
    vector<int> successfulPairs(vector<int>& spells, vector<int>& potions, long long success) {
        int n = spells.size();
        vector<int> ans(n) ;
        sort(potions.begin(),potions.end());
        for(int i =0;i<n;i++){
            long long remaining = success/spells[i] ;
            int pos;
            if(remaining* spells[i] == success ) {
                pos = lower_bound(potions.begin(),potions.end(),remaining) - potions.begin();
            }
            else{
                pos = upper_bound(potions.begin(),potions.end(),remaining) - potions.begin();

            }
            ans[i]=potions.size() - pos;
        }
        return ans ;
    }
};

